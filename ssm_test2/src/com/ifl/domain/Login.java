package com.ifl.domain;

public class Login {
	private String logId;
	private String pswd;
	/**
	 * @return logId
	 */
	public String getlogId() {
		return logId;
	}
	/**
	 * @param logId 要设置的 logId
	 */
	public void setlogId(String logId) {
		this.logId = logId;
	}
	/**
	 * @return pswd
	 */
	public String getPswd() {
		return pswd;
	}
	/**
	 * @param pswd 要设置的 pswd
	 */
	public void setPswd(String pswd) {
		this.pswd = pswd;
	}
	
}
